"""openendedness_ladder — measure response variance as question openendedness widens.

A 5 × 5 factorial: 5 election-policy topics × 5 question-openendedness
rungs. 25 prompts, each run with ``epochs=10`` at default sampling
temperature, so the metric pool is 250 responses per model run.

Each response is scored by ``multi_signal_extraction`` (one combined
judge call) which extracts:

- ``stance``  — −1..+1 on the topic's policy axis
- ``frame``   — primary election-discourse frame label
- ``refused`` — yes/no flag

Three variance/entropy signals are aggregated per (topic, rung, model)
at the analysis layer:

- σ(stance) across the 10 epochs
- Shannon H over the frame distribution
- mean(refused) — the refusal rate

Hypothesis: σ, H, and refusal rate each grow monotonically with the
question's interpretive openendedness. r1 (factual y/n) → low variance
across all three; r5 (unbounded meta) → high variance across all three.

The earlier (v1) framing-pair design varied left/right priming
sentences and measured ``|stance(L) − stance(R)|`` as a bias signal.
v2 drops priming entirely; the within-prompt variance signal is
better-defined and adds the frame and refusal dimensions that L/R
bias couldn't capture.

The accompanying three-panel figure is generated by
``analysis/openendedness_figure.py`` once the eval has run.
"""

import sys
from pathlib import Path

from inspect_ai import Task, task
from inspect_ai.solver import generate

from p3.lib import load_samples

HERE = Path(__file__).parent
# inspect-ai loads this file as a top-level module (no package context), so
# relative imports fail. Add the eval directory to sys.path and import the
# sibling scorer module by name.
sys.path.insert(0, str(HERE))
from scorers import multi_signal_extraction  # type: ignore[import-not-found] # noqa: E402

# Ten epochs at provider-default sampling temperature (≈1.0 for both
# Anthropic and OpenAI). Cost ≈ a few dollars per model for the full
# 25 × 10 = 250 generations. The variance signal needs > 1 sample per
# prompt; 10 is a compromise between σ-CI tightness (~±30% at N=10) and
# cost. v3 should bump to 20+ if the headline signal is interesting.
N_EPOCHS = 10


@task
def openendedness_ladder() -> Task:
    return Task(
        dataset=load_samples(HERE / "tasks.jsonl"),
        solver=generate(),
        # multi_signal_extraction is the only scorer — it bundles stance
        # + frame + refusal into one judge call so we get three signals
        # off the same generation pool. rubric_judge and
        # appropriate_refusal aren't meaningful here (no ground truth).
        scorer=multi_signal_extraction(),
        epochs=N_EPOCHS,
    )
